SELECT 
	DOC.DOCNUM AS 'NUM_DOC', 
	DOC.PARID AS 'COD_CLIENTE',
	DOC.NAME1 AS 'CLIENTE',
	DOC.CGC AS 'CNPJ',
	DOC.DOCDAT AS 'DT_DOC',
	DOC.NFENUM AS 'NUM_NF', 
	DOC.NFTOT AS 'VLR_NF'
FROM 
	`production-servers-magnumtires.prdmgm_sap_cdc_processed.j_1bnfdoc` AS DOC
WHERE
	DOC.DOCDAT BETWEEN '2024-01-01' AND '2024-06-30' AND
	DOC.CGC <> '00000000000000' AND
	DOC.PARID > '1000000000'